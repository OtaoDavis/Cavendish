    <script>
      document.addEventListener("DOMContentLoaded", function () {
        const preloader = document.getElementById("aeh-loader");

        const hideLoader = () => {
          setTimeout(() => {
            if (preloader) preloader.classList.add("loader-finished");
            document.body.classList.remove("content-hidden");
          }, 1000);
        };

        if (document.readyState === "complete") {
          hideLoader();
        } else {
          window.addEventListener("load", hideLoader);
        }

        const internalLinks = document.querySelectorAll(
          'a[href^="index.php"], a[href^="about.php"], a[href^="talent.php"], a[href^="partnerships.php"]',
        );

        internalLinks.forEach((link) => {
          link.addEventListener("click", (e) => {
            if (!link.hash && !e.metaKey && !e.ctrlKey) {
              e.preventDefault();
              const destination = link.href;
              if (preloader) preloader.classList.remove("loader-finished");
              setTimeout(() => {
                window.location.href = destination;
              }, 500);
            }
          });
        });
      });
    </script>
